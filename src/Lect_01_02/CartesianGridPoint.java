package Lect_01_02;

public class CartesianGridPoint {
    private final double x;
    private final double y;

    public CartesianGridPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // getters
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}